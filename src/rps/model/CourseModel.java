package rps.model;

public class CourseModel {
	
	int cid;
	String ctl;
	int ccd;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCtl() {
		return ctl;
	}
	public void setCtl(String ctl) {
		this.ctl = ctl;
	}
	public int getCcd() {
		return ccd;
	}
	public void setCcd(int ccd) {
		this.ccd = ccd;
	}
}
