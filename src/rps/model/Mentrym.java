package rps.model;

public class Mentrym {
	int id;
	int ccd;
	String yr;
	int marks;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCcd() {
		return ccd;
	}
	public void setCcd(int ccd) {
		this.ccd = ccd;
	}
	public String getYr() {
		return yr;
	}
	public void setYr(String yr) {
		this.yr = yr;
	}
	public int getMarks() {
		return marks;
	}
	public void setMarks(int marks) {
		this.marks = marks;
	}

}
